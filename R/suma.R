#' Suma dos numeros
#'
#' La función `suma()` toma 2 números positivos y los suma.
#'
#' Estos son detalles extras de la función.
#'
#' @param x un vector numérico positivo
#' @param y un vector numérico
#'
#' @return
#' Un vector numérico con la suma de `x` e `y`.
#'
#' @examples
#' suma(2, 2)
#' suma(1, 10)
#'
#'
#' @export
suma <- function(x, y) {
  return(x + y)
}
